public class HinhChuNhat {
    public double Chieudai;
    public double Chieurong;
    
    public double DienTich()
    {
        return Chieudai * Chieurong;
    }
    public double ChuVi()
    {
        return (Chieudai * Chieurong)*2;
    }
}
